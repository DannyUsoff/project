# Входные параметры
U1 = 10    # кВ (входное напряжение)
U2 = 0.4   # кВ (выходное напряжение)
I1 = 1.7   # A (входной ток)
I2 = 39  # A (выходной ток)
P_nom = 15 # кВт (номинальная мощность)
cos_φ = 0.85 # (коэффициент мощности)

# Пересчет в базовые единицы измерения
U1_v = U1 * 1000  # переводим кВ в В
U2_v = U2 * 1000  # переводим кВ в В
P_nom_w = P_nom * 1000  # переводим кВт в Вт

# Расчет мощностей
input_power = U1_v * I1 * cos_φ     # входная мощность (ВА)
output_power = U2_v * I2 * cos_φ    # выходная мощность (ВА)
active_power = P_nom_w      # активная мощность (Вт)

# Расчет коэффициентов
efficiency = (output_power / input_power) * 100  # КПД в %
load_factor = (output_power / P_nom_w) * 100     # коэффициент загрузки

# Проверка режима работы
if load_factor > 100:
    work_mode = "ПЕРЕГРУЗКА"
elif load_factor > 80:
    work_mode = "ОПТИМАЛЬНЫЙ РЕЖИМ"
else:
    work_mode = "НЕДОГРУЗКА"

# Вывод результатов
print(f"Анализ эффективности подстанции:\n{'='*30}")
print(f"Входная мощность: {input_power/1000:.1f} кВА")
print(f"Выходная мощность: {output_power/1000:.1f} кВА")
print(f"Активная мощность: {active_power/1000} кВт")
print(f"КПД подстанции: {efficiency:.1f}%")
print(f"Коэффициент загрузки: {load_factor:.1f}%")
print(f"Режим работы: {work_mode}")

# Дополнительная проверка
if efficiency > 95:
    print("\nРекомендация: Показатели эффективности в норме")
else:
    print("\nРекомендация: Требуется диагностика оборудования")